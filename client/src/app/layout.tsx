// };

function RootLayout({ children }: { children: React.ReactNode }) {
  const pathname = usePathname();
  const router = useRouter();
  const loginPath = [
    '/login',
    '/register',
    '/forgot-password',
    '/find',
    '/social-register',
    '/redirection',
  ];

  useEffect(() => {
    const fetchData = async () => {
      const tokenData = await handleConfirmToken();
      if (!tokenData && !loginPath.includes(pathname)) {
        router.push('/login');
      }
    };
    fetchData();
  }, [pathname]);

  useEffect(() => {
    setScreenSize();
  }, []);

  function setScreenSize() {
    const wrapElement: any = document.querySelector('.wrap');
    wrapElement.style.height = window.innerHeight + 'px';
  }
  const [refreshing, setRefreshing] = useState(false);

  const handleRefresh = () => {
    // 리프레시 작업 수행
    setRefreshing(true);

    // 예: 2초 후에 리프레시 종료
    setTimeout(() => {
      setRefreshing(false);
    }, 2000);
  };

  return (
    <Provider store={store}>
      <html lang="en">
        <body className={`${inter.className} wrap`}>
          {/* <body className={`${inter.className} h-full`}> */}

          <main className="h-[calc(100%-56px)] w-screen flex flex-col	items-center justify-center">
            {/* <PullToRefresh onRefresh={handleRefresh}>{children}</PullToRefresh> */}
            {children}
          </main>
          {loginPath.includes(pathname) || <Navbar></Navbar>}
          <div id="portal"></div>
        </body>
      </html>
    </Provider>
  );
}

export default RootLayout;
